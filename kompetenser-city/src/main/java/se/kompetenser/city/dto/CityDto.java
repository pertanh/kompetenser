package se.kompetenser.city.dto;

public class CityDto {
	
	private Integer cityId;
	private String name;

	public Integer getId() {
		return cityId;
	}
	public void setId(Integer id) {
		this.cityId = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
